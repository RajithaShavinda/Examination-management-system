    private void LoadTbl() {
        try {

            //get current table model and clear all 
            DefaultTableModel dm1 = (DefaultTableModel) jTable_student.getModel();
            dm1.getDataVector().removeAllElements();
            dm1.fireTableDataChanged();

            java.sql.Connection con = ConnectionDB.Connect();

            //get all students detail
            String sql = "SELECT * FROM `student` where type='std'";

            //java.sql.Statement>> 'Statement' object used for executing a static SQL statement and returning the results it produces.
            java.sql.Statement state = con.createStatement();

            //'ResultSet' >> A table of data representing a database result set, which is usually generated by executing a 'Statement' that queries the database.
            ResultSet rs = state.executeQuery(sql);

            DefaultTableModel model = (DefaultTableModel) jTable_student.getModel(); //get current table model || table object

            while (rs.next()) {

                //add student details to the table row by row
                model.addRow(new Object[]{rs.getString("ID"), rs.getString("Name"),
                    rs.getString("email"), rs.getString("Contact"), rs.getString("Cname")});

                jTable_student.setModel(model);
            }

            state.close(); //Statement

            rs.close(); // ResultSet

        } catch (SQLException se) {
            JOptionPane.showMessageDialog(null, se.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        //---------------------------------------------------------------------
    }

