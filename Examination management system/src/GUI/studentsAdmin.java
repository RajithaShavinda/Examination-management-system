    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int row = -1;
        String id;
        row = jTable_student.getSelectedRow(); //get select row number

        if (row == -1) {

            JOptionPane.showMessageDialog(null, "Please select an item");

        } else {

            try {

                id = jTable_student.getValueAt(row, 0).toString();//get selected item's id
                java.sql.Connection con = ConnectionDB.Connect();

                //get student details by id
                String sql = "SELECT * FROM `student` where ID='" + id + "'";

                java.sql.Statement state = con.createStatement();

                ResultSet rs = state.executeQuery(sql);

                rs.next();

                //creating Student object using Result set's data
                std = new Student(rs.getString("ID"), rs.getString("Name"), rs.getString("email"),
                        rs.getString("contact"), rs.getString("Cname"), rs.getString("bday"), rs.getString("password"),
                        rs.getString("subjects"), rs.getString("subNames"));

                state.close(); //Statement

                rs.close(); // ResultSet

                Register r = new Register();
                r.init(std, true);//send student objects to "Register" interface
                r.setPasswordFields(true);// enable update password option
                r.setVisible(true);
                this.dispose();

            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, se.getMessage());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int row = -1;
        String id;
        row = jTable_student.getSelectedRow(); //get select row number

        if (row == -1) {

            JOptionPane.showMessageDialog(null, "Please select an item");

        } else {

            try {
                //get confirmation message's answer to the "dialogButton1" variable (yes || no)
                   int dialogButton1 = JOptionPane.showConfirmDialog(null, "Are you sure you want to permanently delete this record?", "Delete", JOptionPane.YES_NO_OPTION);

                if (dialogButton1 == 0) { //if user click yes

                    

                id = jTable_student.getValueAt(row, 0).toString();//get selected item's id
                
                java.sql.Connection con = ConnectionDB.Connect();
                
                //query: delete student record from the table
                String str = "delete from `student` Where `ID`='" + id + "';";

                Statement statement = con.createStatement();

                statement.executeUpdate(str);//delete

                statement.close();

                JOptionPane.showMessageDialog(null, "Successfull");
                LoadTbl();//Re-load table
                }
            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, se.getMessage());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

